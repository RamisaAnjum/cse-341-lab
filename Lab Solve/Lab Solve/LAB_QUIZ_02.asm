.MODEL SMALL
 
.STACK 100H

.DATA 
X DB "FIZZ$"
Y DB "BUZZ$"
Z DB "FIZZBUZZ$" 



.CODE 
MAIN PROC 

;iniitialize DS

MOV AX,@DATA 
MOV DS,AX      

;INPUT01
MOV AH,1
INT 21H
MOV BL,AL

MOV DL,0Dh
MOV AH,2
INT 21H      
MOV DL,0Ah
MOV AH,2
INT 21H
;INPUT02
MOV AH,1
INT 21H
MOV CL,AL

MOV DL,0Dh
MOV AH,2
INT 21H      
MOV DL,0Ah
MOV AH,2
INT 21H
;SUBSTRACT
SUB BL,30h
SUB CL,30h

;SWAP
MOV AL,BL 

;START

DIV CL
MOV BL,AL

MOV AH,0

MOV CL,2
DIV CL
CMP AH,0
JE LEVEL1

MOV AH,0
MOV AL,BL
MOV CL,3
DIV CL
CMP AH,0
JE LEVEL2


LEVEL1:
LEA DX,X
MOV AH,9
INT 21H
 

MOV AL,BL
MOV CL,3
DIV CL
CMP AH,0
JE LEVEL3
JMP endif 

LEVEL2:
LEA DX,Y
MOV AH,9
INT 21H
JMP endif



LEVEL3:
LEA DX,Z
MOV AH,9
INT 21H



endif:










;exit to DOS 
               
MOV AX,4C00H
INT 21H 

MAIN ENDP
    END MAIN 
  



